package awesome.app.data;

import java.util.List;

public class MenuData {
	private List<String> breakfastEntrees;
	private List<String> breakfastSides;
	private List<String> lunchEntrees;
	private List<String> lunchSides;
	private List<String> dinnerEntrees;
	private List<String> dinnerSides;

	public List<String> getBreakfastEntrees() {
		return breakfastEntrees;
	}

	public void setBreakfastEntrees(List<String> breakfastEntrees) {
		this.breakfastEntrees = breakfastEntrees;
	}

	public List<String> getBreakfastSides() {
		return breakfastSides;
	}

	public void setBreakfastSides(List<String> breakfastSides) {
		this.breakfastSides = breakfastSides;
	}

	public List<String> getLunchEntrees() {
		return lunchEntrees;
	}

	public void setLunchEntrees(List<String> lunchEntrees) {
		this.lunchEntrees = lunchEntrees;
	}

	public List<String> getLunchSides() {
		return lunchSides;
	}

	public void setLunchSides(List<String> lunchSides) {
		this.lunchSides = lunchSides;
	}

	public List<String> getDinnerEntrees() {
		return dinnerEntrees;
	}

	public void setDinnerEntrees(List<String> dinnerEntrees) {
		this.dinnerEntrees = dinnerEntrees;
	}

	public List<String> getDinnerSides() {
		return dinnerSides;
	}

	public void setDinnerSides(List<String> dinnerSides) {
		this.dinnerSides = dinnerSides;
	}

}
